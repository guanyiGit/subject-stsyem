package com.system.pojo.rt;

import java.util.List;

public class SubScore {
    /**
     * 题目序号
     */
    private Integer optSeq;
    /**
     * 题目id
     */
    private Integer subjectId;
    /**
     * 题目内容
     */
    private String content;

    /**
     * 题目类型
     */
    private Integer subjectType;

    /**
     * 题目图片url
     */
    private String subjectUrl;

    /**
     * 考生答案集合
     */
    private List<SourceResults> sourceResults;

    /**
     * 选项
     */
    private List<SubScoreOpts> subScoreOpts;

    public Integer getOptSeq() {
        return optSeq;
    }

    public void setOptSeq(Integer optSeq) {
        this.optSeq = optSeq;
    }

    public Integer getSubjectId() {
        return subjectId;
    }

    public void setSubjectId(Integer subjectId) {
        this.subjectId = subjectId;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getSubjectUrl() {
        return subjectUrl;
    }

    public void setSubjectUrl(String subjectUrl) {
        this.subjectUrl = subjectUrl;
    }

    public List<SubScoreOpts> getSubScoreOpts() {
        return subScoreOpts;
    }

    public List<SourceResults> getSourceResults() {
        return sourceResults;
    }

    public void setSourceResults(List<SourceResults> sourceResults) {
        this.sourceResults = sourceResults;
    }

    public void setSubScoreOpts(List<SubScoreOpts> subScoreOpts) {
        this.subScoreOpts = subScoreOpts;
    }

    public Integer getSubjectType() {
        return subjectType;
    }

    public void setSubjectType(Integer subjectType) {
        this.subjectType = subjectType;
    }
}
