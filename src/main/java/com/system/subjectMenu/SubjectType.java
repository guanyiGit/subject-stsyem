package com.system.subjectMenu;

/**
 * 题目类型
 * @author pengyu
 * @Date 2019/7/18
 * @Description
 */
public enum SubjectType {
    /**
     * 单选
     */
    SingleChoice(0,"单选"),

    /**
     * 多选
     */
    MultipleChoice(1,"多选"),

    /**
     * 判断
     */
    Judgment(2,"判断"),

    /**
     * 简答
     */
    BriefAnswer(3,"简答");

    private Integer type;

    private String  typeDes;

    SubjectType(Integer type, String typeDes) {
        this.type = type;
        this.typeDes = typeDes;
    }

    public String getTypeDes() {
        return typeDes;
    }

    public Integer getType() {
        return type;
    }

}
